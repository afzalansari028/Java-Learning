package Basics;

public class ArithmeticOperators {

	public static void main(String[] args) {
		int a = 20;
		int b = 10;
		int c = a+b;
		    
		    System.out.println("Sum is "+c);
		    c = a-b;
		    System.out.println("Sub is "+c);
		    c = a*b;
		    System.out.println("multiply is "+c);
		    c = a/b;
		    System.out.println("division is "+c);

	}

}
